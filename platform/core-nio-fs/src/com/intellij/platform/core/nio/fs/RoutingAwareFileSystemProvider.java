package com.intellij.platform.core.nio.fs;

import org.jetbrains.annotations.NotNull;

import java.nio.file.CopyOption;
import java.nio.file.Path;
import java.nio.file.spi.FileSystemProvider;

/**
 * A mix-in interface for {@link FileSystemProvider}, which is checked by {@link MultiRoutingFileSystemProvider}.
 */
public interface RoutingAwareFileSystemProvider {
  /**
   * This method should never be called with a {@code path} that belongs to {@code this} filesystem. If this rule is violated, behavior is undefined.
   *
   * @param path A path from another file system.
   *
   * @return true if this file system provider can handle the path generated by the other file system provider.
   * For example, if {@code this} is able to handle {@link FileSystemProvider#copy(Path, Path, CopyOption...)} with the path from another filesystem.
   */
  boolean canHandleRouting(@NotNull Path path);
}
