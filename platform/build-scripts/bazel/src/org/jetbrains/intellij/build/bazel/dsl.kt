package org.jetbrains.intellij.build.bazel

internal inline fun buildFile(out: BazelFileUpdater, sectionName: String, block: BuildFile.() -> Unit) {
  out.insertAutoGeneratedSection(sectionName = sectionName, autoGeneratedContent = BuildFile().apply(block).render())
}

internal interface Renderable {
  fun render(): String
}

internal open class BuildFile {
  private val loads = HashSet<LoadStatement>()
  private val targets = mutableListOf<Target>()
  private val exportsFiles = mutableSetOf<String>()
  private val lines = mutableListOf<String>()

  val loadStatements: List<LoadStatement>
    get() = loads.toList()

  fun load(bzlFile: String, vararg symbols: String) {
    val existing = loads.firstOrNull { it.bzlFile == bzlFile }
    if (existing == null) {
      loads.add(LoadStatement(bzlFile, symbols.toList()))
    }
    else {
      loads.remove(existing)
      loads.add(LoadStatement(bzlFile, (symbols.toList() + existing.symbols).sorted().distinct()))
    }
  }

  fun addTarget(target: Target) {
    targets.add(target)
  }

  fun target(type: String, block: Target.() -> Unit) {
    val target = Target(type).apply(block)
    addTarget(target)
  }

  fun exportFile(path: String) {
    exportsFiles.add(path)
  }

  open fun render(existingLoads: Map<String, Set<String>> = emptyMap()): String {
    val filteredLoads = loads.mapNotNull { load ->
      val filteredSymbols = load.symbols.filter { existingLoads[load.bzlFile]?.contains(it) != true }
      if (filteredSymbols.isEmpty()) {
        null
      }
      else {
        LoadStatement(load.bzlFile, filteredSymbols)
      }
    }

    val loadStatements = filteredLoads
      .map { it.render() }
      .sorted()
      .joinToString("\n")
    val targetStatements = (targets.joinToString("\n") { it.render() }).trim()

    val exportsFiles = exportsFilesSection(exportsFiles)

    val render = sequenceOf(loadStatements, targetStatements, exportsFiles, lines.joinToString("\n"))
      .filter { it.isNotEmpty() }
      .joinToString("\n\n")

    return render
  }
}

private fun exportsFilesSection(exportsFiles: Set<String>) : String {
  return if (exportsFiles.isNotEmpty()) {
    buildString {
      appendLine("exports_files(")
      appendLine("$INDENT[")
      exportsFiles.forEach { appendLine("$INDENT$INDENT\"$it\",") }
      appendLine("$INDENT],")
      appendLine("${INDENT}visibility = [\"//visibility:public\"],")
      appendLine(")")
    }
  }
  else ""
}

// Class to represent a load statement
internal data class LoadStatement(@JvmField val bzlFile: String, @JvmField val symbols: List<String>) : Renderable {
  override fun render(): String {
    val formattedSymbols = symbols.sorted().joinToString(", ") { "\"$it\"" }
    return """load("$bzlFile", $formattedSymbols)"""
  }
}

internal class Target(private val type: String) : Renderable {
  private val attributes = LinkedHashMap<String, Any>()

  // Support for setting key-value pairs with arrays
  fun option(key: String, value: Any) {
    verifyTypeIsSupported(value)

    if (attributes.containsKey(key)) {
      error("Duplicate key: $key. Old value: ${attributes[key]}, new value: $value")
    }

    attributes[key] = value
  }

  private fun verifyTypeIsSupported(value: Any) {
    val klass = value.javaClass
    when {
      String::class.java.isAssignableFrom(klass) -> Unit
      value == true || value == false -> Unit
      Renderable::class.java.isAssignableFrom(klass) -> Unit
      List::class.java.isAssignableFrom(klass) -> {
        for (item in value as List<*>) {
          verifyTypeIsSupported(item!!)
        }
      }
      LinkedHashSet::class.java.isAssignableFrom(klass) -> {
        for (item in value as LinkedHashSet<*>) {
          verifyTypeIsSupported(item!!)
        }
      }
      HashMap::class.java.isAssignableFrom(klass) -> {
        for (item in value as Map<*, *>) {
          verifyTypeIsSupported(item.key!!)
          verifyTypeIsSupported(item.value!!)
        }
      }
      DoNotSortIndicator::class.java.isAssignableFrom(klass) -> Unit
      else -> error("Unsupported type '$klass' for value: $value")
    }
  }

  fun visibility(targets: Array<String>) {
    option("visibility", targets.toList())
  }

  internal fun glob(list: List<String>, exclude: List<String> = emptyList(), allowEmpty: Boolean = true): Renderable {
    return object : Renderable {
      override fun render(): String {
        return buildString {
          appendLine("glob(")
          appendFormattedList(elements = list, nesting = 2)
          if (allowEmpty) {
            appendLine("$INDENT${INDENT}allow_empty = True,")
          }
          if (exclude.isNotEmpty()) {
            appendFormattedList(name = "exclude", elements = exclude, nesting = 2)
          }
          append("$INDENT)")
        }
      }
    }
  }

  private fun StringBuilder.appendFormattedList(name: String? = null, elements: List<String> = emptyList(), nesting: Int = 0) {
    val localIndent = INDENT.repeat(nesting)
    val opening = name?.let { "$localIndent$name = " } ?: localIndent
    when (elements.size) {
      0 -> appendLine("$opening[],")
      1 -> appendLine("$opening[${formatValue(elements.single())}],")
      else -> {
        appendLine("$opening[")
        elements.forEach { item ->
          appendLine("$localIndent$INDENT${formatValue(item)},")
        }
        appendLine("$localIndent],")
      }
    }
  }

  override fun render(): String {
    val renderedAttributes = attributes.map { (key, value) ->
      "$INDENT$key = ${formatValue(value)},"
    }.joinToString("\n")

    return buildString {
      if (type.isEmpty()) {
        appendLine(renderedAttributes)
      }
      else {
        appendLine("$type(")
        appendLine(renderedAttributes)
        appendLine(")")
      }
    }
  }
}

private fun formatValue(value: Any?): String {
  return when (value) {
    is Collection<*> -> {
      if (value.size > 1) {
        value.joinToString(separator = ",\n$INDENT$INDENT", prefix = "[\n$INDENT$INDENT", postfix = ",\n$INDENT]") { formatValue(it) }
      }
      else {
        value.joinToString(separator = ", ", prefix = "[", postfix = "]") { formatValue(it) }
      }
    }
    is Array<*> -> value.joinToString(separator = ", ", prefix = "[", postfix = "]") { formatValue(it) }
    is String -> "\"$value\""
    is Number -> value.toString()
    is Map<*, *> -> value.entries.joinToString(",\n$INDENT$INDENT", prefix = "{\n$INDENT$INDENT", postfix = ",\n$INDENT}") {
      (key, value) -> "\"$key\": ${formatValue(value)}"
    }
    true -> "True"
    false -> "False"
    is Renderable -> value.render()
    is DoNotSortIndicator -> "# do not sort"
    else -> value.toString()
  }
}

internal const val INDENT = "    "
internal object DoNotSortIndicator

internal fun List<Any>.unsorted(): List<Any> {
  return if (this.size < 2) {
    this
  } else {
    listOf<Any>(DoNotSortIndicator) + this
  }
}
