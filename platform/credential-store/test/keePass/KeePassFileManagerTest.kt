  @Test
  fun `require custom master password if need - not custom`() {
    val store = createStore()
    store.save(defaultEncryptionSpec)
    TestKeePassFileManager(store, masterPasswordRequestAnswer = "bar42").setCustomMainPasswordIfNeeded(fsRule.fs.getPath("/someDefault"))
    assertThat(MainKeyFileStorage(store.mainKeyFile).load()!!.toString(Charsets.UTF_8)).isEqualTo("bar42")
    assertThat(createTestStoreWithCustomMasterKey().get(testCredentialAttributes)!!.password!!.toString()).isEqualTo("p")
  }

  @Test
  fun `require custom master password if need - custom`() {
    val store = createTestStoreWithCustomMasterKey()
    TestKeePassFileManager(store, masterPasswordRequestAnswer = "bar42").setCustomMainPasswordIfNeeded(fsRule.fs.getPath("/someDefault"))
    assertThat(MainKeyFileStorage(store.mainKeyFile).load()!!.toString(Charsets.UTF_8)).isEqualTo("foo")
