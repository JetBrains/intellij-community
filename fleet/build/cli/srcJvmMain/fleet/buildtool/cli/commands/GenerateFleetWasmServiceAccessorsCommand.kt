package fleet.buildtool.cli.commands

import com.github.ajalt.clikt.core.CliktCommand
import com.github.ajalt.clikt.parameters.options.option
import com.github.ajalt.clikt.parameters.options.multiple as multipleOptions
import com.github.ajalt.clikt.parameters.options.required
import com.github.ajalt.clikt.parameters.types.path
import com.google.devtools.ksp.processing.KSPJsConfig
import fleet.buildtool.cli.executeKsp
import fleet.buildtool.cli.findSourceRoot
import java.nio.file.Files
import java.nio.file.Path
import kotlin.io.path.ExperimentalPathApi
import kotlin.io.path.copyTo
import kotlin.io.path.createDirectories
import kotlin.io.path.deleteRecursively
import kotlin.io.path.isRegularFile
import kotlin.io.path.name
import kotlin.io.path.walk
import kotlin.io.path.writeText

// Name of the Kotlin file generated by `FleetKspProcessor` (`ServiceProvider.wasm` + `.kt`).
private const val GENERATED_ACCESSORS_FILE_NAME = "ServiceProvider.wasm.kt"

/**
 * Runs `FleetKspProcessor` over a WasmJS module's sources and emits the generated `ServiceProvider.wasm.kt`
 * (the `@JsExport fun <module>_findServices_<service>()` accessors that `FleetModuleLayer.wasm.kt` discovers at
 * runtime). This is the Bazel counterpart of the KSP wiring of the Gradle WasmJS compilations.
 *
 * The analysis runs on the WasmJS platform with the module's compile-visible dependency klibs as `--classpath`,
 * mirroring the module's own WasmJS compilation. When the module declares no services, an empty placeholder file
 * is written so the output stays a valid Kotlin source.
 */
class GenerateFleetWasmServiceAccessorsCommand : CliktCommand(
  name = "generate-fleet-wasm-service-accessors",
) {
  private val sources by option("--sources").path().multipleOptions()
  private val classpath by option("--classpath").path().multipleOptions()
  private val processorClasspath by option("--processor-classpath").path().multipleOptions(required = true)
  private val moduleName by option("--module-name").required()
  private val languageVersion by option("--language-version").required()
  private val apiVersion by option("--api-version").required()
  private val outputFile by option("--output-file").path().required()

  @OptIn(ExperimentalPathApi::class)
  override fun run() {
    val projectBaseDirPath = Path.of("").toAbsolutePath().normalize()
    val workDir = Files.createTempDirectory(projectBaseDirPath, "fleet-wasm-service-accessors-generator")
    try {
      val normalizedSources = sources.map { it.toAbsolutePath().normalize() }
      val normalizedClasspath = classpath.map { it.toAbsolutePath().normalize() }
      val normalizedProcessorClasspath = processorClasspath.map { it.toAbsolutePath().normalize() }
      val classesOutputDirPath = Files.createDirectories(workDir.resolve("classes"))
      val kotlinOutputDirPath = Files.createDirectories(workDir.resolve("kotlin"))
      val resourceOutputDirPath = Files.createDirectories(workDir.resolve("resources"))
      val cachesDirPath = Files.createDirectories(workDir.resolve("caches"))
      val sourceRootPaths = normalizedSources.map { findSourceRoot(it) }.distinct()
      val commonSourceRootPaths = sourceRootPaths.filter { it.fileName.toString().contains("Common", ignoreCase = true) }
      val platformSourceRootPaths = sourceRootPaths - commonSourceRootPaths.toSet()
      @Suppress("IO_FILE_USAGE") val kspConfig = KSPJsConfig.Builder().apply {
        backend = "WASM"
        moduleName = this@GenerateFleetWasmServiceAccessorsCommand.moduleName
        sourceRoots = platformSourceRootPaths.map { it.toFile() }
        commonSourceRoots = commonSourceRootPaths.map { it.toFile() }
        libraries = normalizedClasspath.map { it.toFile() }
        projectBaseDir = projectBaseDirPath.toFile()
        outputBaseDir = workDir.toFile()
        cachesDir = cachesDirPath.toFile()
        classOutputDir = classesOutputDirPath.toFile()
        kotlinOutputDir = kotlinOutputDirPath.toFile()
        resourceOutputDir = resourceOutputDirPath.toFile()
        languageVersion = this@GenerateFleetWasmServiceAccessorsCommand.languageVersion
        apiVersion = this@GenerateFleetWasmServiceAccessorsCommand.apiVersion
      }.build()

      val kspExitCode = executeKsp(kspConfig, normalizedProcessorClasspath)
      check(kspExitCode == 0) {
        "KSP failed with exit code $kspExitCode"
      }

      val accessorFiles = kotlinOutputDirPath.walk().filter { it.isRegularFile() && it.name == GENERATED_ACCESSORS_FILE_NAME }.toList()
      outputFile.parent?.createDirectories()
      when (accessorFiles.size) {
        0 -> outputFile.writeText("// No Fleet services are declared in module `$moduleName`.\n")
        1 -> accessorFiles.single().copyTo(outputFile, overwrite = true)
        else -> error("Multiple generated Wasm service accessor files: $accessorFiles")
      }
    }
    finally {
      workDir.deleteRecursively()
    }
  }
}
