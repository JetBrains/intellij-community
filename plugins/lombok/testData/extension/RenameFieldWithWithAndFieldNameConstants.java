import lombok.With;
import lombok.experimental.FieldNameConstants;

/**
 * Test class for renaming fields in a class with both @With and @FieldNameConstants annotations
 */
@FieldNameConstants
public class RenameFieldWithWithAndFieldNameConstants {
  @With
  private final String oldField;
  private int count;

  public RenameFieldWithWithAndFieldNameConstants(String oldField, int count) {
    this.oldField = oldField;
    this.count = count;
  }

  public void printInfo() {
    System.out.println("Field value: " + oldField + ", Count: " + count);
  }

  public void useWithMethod() {
    // Using the with method generated by Lombok
    RenameFieldWithWithAndFieldNameConstants updated = withOldField("new value");
    System.out.println("Updated field: " + updated.oldField);
  }

  public void useFieldNameConstants() {
    // Using the field name constants generated by Lombok
    System.out.println("Field name constant: " + Fields.oldField);

    // Using the field name constants in a method
    validateField(Fields.oldField);
  }

  private void validateField(String fieldName) {
    System.out.println("Validating field: " + fieldName);
  }
}