package com.intellij.mermaid.jcef

import com.intellij.mermaid.api.MermaidRenderResult
import com.intellij.mermaid.api.appendTo
import com.intellij.mermaid.jcef.impl.decode
import kotlinx.browser.window
import kotlinx.coroutines.coroutineScope
import kotlinx.coroutines.ensureActive
import kotlinx.coroutines.flow.collectLatest
import kotlinx.coroutines.flow.map
import kotlinx.coroutines.flow.merge
import kotlinx.coroutines.flow.onStart
import org.w3c.dom.Element
import org.w3c.dom.HTMLElement
import org.w3c.dom.asList
import kotlin.time.ExperimentalTime
import kotlin.time.measureTime

private fun collectBlocks(): List<HTMLElement> {
  val nodes = window.document.querySelectorAll("div.mermaid").asList()
  // Just for sanity reason
  return nodes.filterIsInstance<HTMLElement>()
}

@OptIn(ExperimentalTime::class)
private suspend fun performRender() {
  val time = measureTime {
    val blocks = collectBlocks()
    console.log("${blocks.size} blocks will be generated by mermaid")
    renderBlocks(blocks)
  }
  console.log("Generated all mermaid blocks in $time")
}

suspend fun renderBlocks(blocks: List<HTMLElement>) {
  coroutineScope {
    for (block in blocks) {
      // Cancellation-safe boundary: drop remaining stale blocks if a newer render arrived. A single
      // block's render is non-cancellable (renderBlock), so renders never overlap (shared global state).
      ensureActive()
      processBlock(block)
    }
  }
}

val nodeToCacheIdAndContent = mutableMapOf<Element, Pair<String, MermaidRenderResult>>()

suspend fun processBlock(block: HTMLElement) {
  block.removeAttribute("data-processed")
  val cacheId = block.getAttribute("data-cache-id")
  checkNotNull(cacheId) { "data-cache-id was not set for block" }
  val content = block.getAttribute("data-actual-fence-content")
  checkNotNull(content) { "data-actual-fence-content was not set for block" }

  if (nodeToCacheIdAndContent[block]?.first == cacheId) {
    val renderResult = nodeToCacheIdAndContent[block]?.second ?: return
    renderResult.appendTo(block)
  } else {
    val actualContent = decode(content)
    val generated = renderBlock(block, cacheId, actualContent) ?: return
    nodeToCacheIdAndContent[block] = cacheId to generated
  }
}

suspend fun markdownExtensionMain() {
  // Cached SVGs are pinned to their render-time max-width, so on a resize we drop the cache and
  // re-render. A single collector serializes patches and resizes so mermaid renders never overlap.
  val patches = IncrementalDom.afterPatchEvents().map { false }
  val resizes = bodyWidthChangeEvents().map { true }
  merge(patches, resizes)
    .onStart { emit(false) } // initial render
    .collectLatest { isResize ->
      if (isResize) {
        nodeToCacheIdAndContent.clear()
      }
      performRender()
    }
}
