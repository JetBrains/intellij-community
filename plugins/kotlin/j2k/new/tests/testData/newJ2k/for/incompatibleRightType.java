public class J {
    public static void main(String[] args) {
        for (int i = 0; i < 10.5; i++) {
            System.out.println(i);
        }
        for (int j = 0; j < 10.5f; j++) {
            System.out.println(j);
        }
        for (int k = 0; k < '5'; k++) {
            System.out.println(k);
        }
    }
}
