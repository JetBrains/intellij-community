#!/bin/bash
# summarize-runs.sh — collate archived per-run leak logs into a summary Markdown.
#
# Reads all $ARCHIVE/run-*.output files produced by leak-loop.sh and writes
# $ARCHIVE/SUMMARY.md containing:
#   - Per-run diagnosis table (tests pass count, leak flag, one-line retention chain).
#   - Per-run full "via '...'" retention block.
#   - The set of UNIQUE retention chain shapes (class-only, values stripped).
#
# Usage:
#   summarize-runs.sh <archive-dir>
# Or set $ARCHIVE and invoke without args.

set -uo pipefail

ARCHIVE="${1:-${ARCHIVE:-}}"
[ -n "$ARCHIVE" ] || { echo "usage: $0 <archive-dir>" >&2; exit 2; }
[ -d "$ARCHIVE" ] || { echo "not a directory: $ARCHIVE" >&2; exit 2; }

REPO="${REPO:-$(cd "$(dirname "$0")/../../../../.." && pwd)}"
RG="$REPO/community/tools/rg.cmd"
if [ ! -x "$RG" ]; then RG=""; fi

_rg() { if [ -n "$RG" ]; then "$RG" "$@"; else grep -E "$@"; fi }

SUMMARY="$ARCHIVE/SUMMARY.md"
UNIQ_TMP="$(mktemp)"
trap 'rm -f "$UNIQ_TMP"' EXIT

{
  echo "# Leak-hunter run summary — $(basename "$ARCHIVE")"
  echo
  echo "Generated by summarize-runs.sh on $(date)."
  echo
  echo "## Per-run diagnosis"
  echo
  echo "| Run | Tests | Leak? | 1-line retention chain (classes+fields, args stripped) |"
  echo "|-----|-------|-------|--------------------------------------------------------|"

  for f in "$ARCHIVE"/run-*.output; do
    [ -f "$f" ] || continue
    idx=$(basename "$f" | sed -E 's/^run-([0-9]+)\.output$/\1/')

    RESULTS=$(sed 's/\x1b\[[0-9;]*m//g' "$f" | grep -oE 'Results: [0-9]+ tests, [0-9]+ passed, [0-9]+ failed' | head -1)
    LEAK_LINES=$(grep -c 'Found a leaked instance' "$f" 2>/dev/null || echo 0)
    if [ "$LEAK_LINES" -gt 0 ]; then LEAK="YES"; else LEAK="no"; fi

    CHAIN=$(_rg -oNI "via '([^']+)'" -r '$1' "$f" 2>/dev/null | head -20 | paste -sd ' -> ' -)
    echo "| $idx | ${RESULTS:-?} | $LEAK | \`$CHAIN\` |"

    _rg -oNI "via '([^']+)'" -r '$1' "$f" 2>/dev/null \
      | sed -E 's/;.*//' \
      | paste -sd ' -> ' - \
      | sed "s/^/run-$idx	/" >> "$UNIQ_TMP"
  done

  echo
  echo "## Unique retention chain shapes"
  echo
  echo "Rows with the same shape are the same leak. Different shapes are independent leaks that need independent fixes."
  echo
  awk -F'\t' '{print $2}' "$UNIQ_TMP" | sort -u | nl -w2 -s'. '

  echo
  echo "## Full per-run retention chains"
  echo
  for f in "$ARCHIVE"/run-*.output; do
    [ -f "$f" ] || continue
    idx=$(basename "$f" | sed -E 's/^run-([0-9]+)\.output$/\1/')
    echo "### Run $idx"
    echo
    echo '```'
    _rg -N '^  via ' "$f" 2>/dev/null | head -25
    echo '```'
    echo
  done

  echo "## Archived files"
  echo
  echo '```'
  ls -la "$ARCHIVE"/*.hprof.zip "$ARCHIVE"/*.output 2>/dev/null
  echo '```'
} > "$SUMMARY"

echo "Wrote $SUMMARY ($(wc -l < "$SUMMARY") lines)"
